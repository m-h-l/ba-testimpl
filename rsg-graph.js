// build2: konstruiert aus den "flachen" Daten in
// graphcontents einen Teilgraph mit id als Startknoten
// die Funktion flat kann den Teilgraph wieder abflachen
build2 = function(cloneFlag, id) {
  var children = [];

  let d = rsgg.graphcontents;
  if (cloneFlag) {
    d = clone(rsgg.graphcontents);
  }

  d.BusinessFunctionRelas.filter(r => r.to === id)
    .map(r => r.from)
    .forEach(n => children.push(build2(cloneFlag, n)));

  d.AppBfRelas.filter(r => r.to === id)
    .map(r => r.from)
    .forEach(n => children.push(build2(cloneFlag, n)));

  d.ProviderRelas.filter(r => r.from === id)
    .map(r => r.to)
    .forEach(n => children.push(build2(cloneFlag, n)));

  d.ConsumerRelas.filter(r => r.from === id)
    .map(r => r.to)
    .forEach(n => children.push(build2(cloneFlag, n)));

  return { id: id, children: children };
};


// flat: Funktionen zum Abflachen der Struktur aus build2
function flat(node) {
  var arr = [];
  node.children.forEach(child => {
    arr.push({ from: node.id, to: child.id });
    flat(child).forEach(c => arr.push(c));
  });
  return arr;
}

