// countFps: FPS kontinuierlich zählen
countFps = function() {
  window.requestAnimFrame = (function() {
    return (
      window.requestAnimationFrame ||
      function(callback) {
        window.setTimeout(callback, 1000 / 60);
      }
    );
  })();

  then = Date.now();
  var render = function() {
    now = Date.now();
    fps = 1 / ((now - then) / 1000.0);
    then = now;

    requestAnimFrame(render);
  };
  render();
};

