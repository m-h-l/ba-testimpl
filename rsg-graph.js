// build: Knoten und Kanten beginnend bei startNode in die 
// Visualisierungsinstanz übergeben
build = function(cloneFlag, startnode, prevnode) {
  let d = rsgg.graphcontents;
  if (cloneFlag) {
    d = clone(rsgg.graphcontents);
  }

  d.BusinessFunctions.filter(f => f.id === startnode).forEach(n => {
    fns.addBFNode(n);
    d.BusinessFunctionRelas.filter(
      r => r.from === startnode && r.to === prevnode
    ).forEach(r => fns.addBFEdge(r));
    d.AppBfRelas.filter(r => r.to === startnode).forEach(r =>
      build(cloneFlag, r.from, r.to)
    );
    d.BusinessFunctionRelas.filter(r => r.to === startnode).forEach(
		r => build(cloneFlag, r.from, r.to)
    );
  });

  d.Applications.filter(f => f.id === startnode).forEach(n => {
    fns.addAppNode(n);
    d.AppBfRelas.filter(
		r => r.to === prevnode && r.from === startnode
	).forEach(
      r => fns.addAppEdge(r)
    );
    d.ConsumerRelas.filter(r => r.from === startnode).forEach(r => {
      d.Interfaces.filter(n => n.id === r.to).forEach(i =>
        fns.addInterfaceNode(i)
      );
      fns.addConsumerEdge(r);
    });
    d.ProviderRelas.filter(r => r.from === startnode).forEach(r => {
      d.Interfaces.filter(n => n.id === r.to).forEach(i =>
        fns.addInterfaceNode(i)
      );
      fns.addProviderEdge(r);
    });
  });
};

