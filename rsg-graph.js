// bench2vis: FPS-Benchmark für Vis.js
bench2vis = function(fns) {
  let tick = f => setInterval(f, 250);
  let pass = () => {};
  fns.setup();
  fns.run(pass);

  let actions = buildTreeActions();

  countFps();

  actions.reduce((acc, item) => {
    return acc.then(() => {
      return new Promise((resolve, reject) => {
        let t = [];
        tick(() => t.push(getFps()));
        let s = Date.now();
        item();
        fns.stabilized(() => {
          let e = Date.now();
          clearInterval();
          fns.stabilized(pass);
          let counts = fns.counts();
          resolve({
            start: s,
            end: e,
            nodes: counts.nodes,
            edges: counts.edges,
            minFps: Math.min(...t),
            maxFps: Math.max(...t),
            avgFps: t.reduce((acc, n) => acc + n, 0) / t.length
          });
        });
      }).then(data => {
        console.log(
          data.nodes,
          data.edges,
          (data.end - data.start) / 1000.0,
          data.minFps,
          data.avgFps,
          data.maxFps
        );
      });
    });
  }, Promise.resolve());
};

